function generateDailyData(days = 30) {
  const data = [];
  const now = new Date();
  for (let i = days - 1; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);
    const dayOfWeek = date.getDay();
    const isWeekend = dayOfWeek === 0 || dayOfWeek === 6;
    const base = isWeekend ? 45 : 120;
    const variance = Math.floor(Math.random() * 60) - 20;
    const trend = Math.floor((days - i) * 1.5);
    data.push({
      date: date.toISOString().split('T')[0],
      conversations: Math.max(10, base + variance + trend),
      apiCalls: Math.max(20, (base + variance + trend) * 3 + Math.floor(Math.random() * 50)),
      resolved: Math.max(5, Math.floor((base + variance + trend) * 0.78)),
      escalated: Math.max(2, Math.floor((base + variance + trend) * 0.22)),
      responseTime: Math.floor(Math.random() * 80 + 120),
    });
  }
  return data;
}

